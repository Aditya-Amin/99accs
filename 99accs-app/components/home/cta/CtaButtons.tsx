const ICON_MAP: Record<CtaPlatform, () => React.ReactElement> = {
  telegram: TelegramIcon,
  discord: DiscordIcon,
};

// --- Single CTA button ---
function CtaButtonLink({ button }: { button: CtaButton }) {
  const Icon = ICON_MAP[button.platform];
  return (
    <a href={button.url} target="_blank" rel="noreferrer">
      {Icon ? <Icon /> : null}
      {button.label}
    </a>
  );
}

// --- Button group ---
export function CtaButtons({ buttons }: { buttons: CtaButton[] }) {
  return (
    <div className="cta__btn">
      {buttons.map((b, index) => (
        <CtaButtonLink key={b.id != null ? b.id : index} button={b} />
      ))}
    </div>
  );
}
